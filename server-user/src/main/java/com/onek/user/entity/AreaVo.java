package com.onek.user.entity;

public class AreaVo {
	private int areac;
	private String arean;
	private String areanh;
	private String qtc;
	private double lat;
	private double lng;
	private int cstatus;
	
	public int getAreac() {
		return areac;
	}
	public void setAreac(int areac) {
		this.areac = areac;
	}
	public String getArean() {
		return arean;
	}
	public void setArean(String arean) {
		this.arean = arean;
	}
	public String getAreanh() {
		return areanh;
	}
	public void setAreanh(String areanh) {
		this.areanh = areanh;
	}
	public String getQtc() {
		return qtc;
	}
	public void setQtc(String qtc) {
		this.qtc = qtc;
	}
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public double getLng() {
		return lng;
	}
	public void setLng(double lng) {
		this.lng = lng;
	}
	public int getCstatus() {
		return cstatus;
	}
	public void setCstatus(int cstatus) {
		this.cstatus = cstatus;
	}
	
	
}
